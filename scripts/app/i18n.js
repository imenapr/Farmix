function safeLocalStorageRead() {
  try {
    return localStorage.getItem(LANG_KEY);
  } catch {
    return null;
  }
}

function safeLocalStorageWrite(lang) {
  try {
    localStorage.setItem(LANG_KEY, lang);
  } catch {
    /* ignore storage failures */
  }
}

export function getCurrentLang() {
  const saved = String(safeLocalStorageRead() ?? "en").toLowerCase();
  return SUPPORTED_LANGS.has(saved) ? saved : "en";
}

export function getCategoryLabel(categoryId, fallbackName = "") {
  if (!categoryId) return t("listing.uncategorized");
  const key = `category.${categoryId}`;
  const lang = getCurrentLang();
  const translated = translations[lang]?.[key] ?? translations.en[key];
  if (translated) return translated;
  if (fallbackName) return fallbackName;
  return String(categoryId);
}

export function t(key, vars = {}) {
  const lang = getCurrentLang();
  const value = translations[lang]?.[key] ?? translations.en[key] ?? vars.default ?? key;
  return String(value).replace(/\{(\w+)\}/g, (_, token) => String(vars[token] ?? ""));
}

export function reRenderUI() {
  if (typeof document !== "undefined" && document.documentElement) {
    document.documentElement.lang = getCurrentLang();
  }
  emit("i18n:changed", { lang: getCurrentLang() });
}

export function setLanguage(lang) {
  const next = SUPPORTED_LANGS.has(lang) ? lang : "en";
  safeLocalStorageWrite(next);
  reRenderUI();
  return next;
}

export function toggleLanguage() {
  const next = getCurrentLang() === "en" ? "ka" : "en";
  return setLanguage(next);
}

export function onLanguageChange(handler) {
  return on("i18n:changed", handler);
}

export function translatePageHead(titleKey, subtitleKey = null) {
  const head = document.querySelector(".page-head");
  if (!head) return;
  const subtitle = subtitleKey ? `<p class="page-subtitle">${t(subtitleKey)}</p>` : "";
  head.innerHTML = `<h1 class="page-title">${t(titleKey)}</h1>${subtitle}`;
}

